import SwiftUI

/// Centralized location for brand-specific colors.
extension Color {
    /// The primary brand color for main action buttons, as per DP-12.
    /// RGB: (227, 143, 188)
    static var brandPink: Color {
        return Color(red: 227 / 255.0, green: 143 / 255.0, blue: 188 / 255.0)
    }
}
